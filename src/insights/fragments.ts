type Key<A extends string, B extends string> = `${A}_${B}`

function combos<A extends string, B extends string>(as: readonly A[], bs: readonly B[]): Key<A, B>[] {
  const out: Key<A, B>[] = []
  for (const a of as) for (const b of bs) out.push(`${a}_${b}` as Key<A, B>)
  return out
}

